#!/usr/bin/env python3
""" concat two string """


def concat(str1: str, str2: str) -> str:
    """
    concat two string and returns the result.

    Args:
       str1 (string) : the first string.
       str2 (string) : the second string.

    Returns:
       string : the concat of str1 and str2.
    """
    return str1 + str2
